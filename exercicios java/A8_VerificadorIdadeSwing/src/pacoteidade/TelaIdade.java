        int idade = 2020 - AN;
        lblIdade.setText(Integer.toString(idade));
        String sit = (idade < 18) ? "MENOR DE IDADE" : "MAIOR DE IDADE";
        lblSit.setText(sit);
        String voto = (((idade >= 16) && (idade < 18) || (idade < 16)) || (idade > 70)) ? "VOTO OPCIONAL" : "VOTO OBRIGATORIO";
        lblVoto.setText(voto);
